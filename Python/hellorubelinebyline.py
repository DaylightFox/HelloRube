for line in spacing:
    text = ""
    c = Char(greeting, char_index_start=0)
    for pos, is_space in enumerate(line):

        # move to next character at every multiple of letter_width (+ 2 empty spaces)
        if (pos != 0) & (pos % (letter_width + 2) == 0):
            c.nextChar()
        
        # add extra whitespace between the words
        if c.current_char == " ":
            text = text + (" ")*5
            c.nextChar()
      
        # add a letter character or whitespace
        if not is_space:
            text = text + (c.current_char)
        else:
            text = text + (" ")
            
    print(text)
